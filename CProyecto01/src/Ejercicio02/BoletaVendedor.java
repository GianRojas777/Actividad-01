    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        double totalVendido = Double.parseDouble(txtTotalVen.getText());
        double sueldoBasico = 350.75;
        double comision = 0.05 * totalVendido;
        double sueldoBruto = sueldoBasico + comision;
        double descuento = 0.15 * sueldoBruto;
        double sueldoNeto = sueldoBruto - descuento;

        resTexArea.setText(
                "Sueldo Básico: S/. " + sueldoBasico + "\n" +
                "Comisión: S/. " + comision + "\n" +
                "Sueldo Bruto: S/. " + sueldoBruto + "\n" +
                "Descuento: S/. " + descuento + "\n" +
                "Sueldo Neto: S/. " + sueldoNeto
        );
    }//GEN-LAST:event_btnCalcularActionPerformed

